    print(f"Результат: {len(grouped)} товарів після групування\n")

    for i, product in enumerate(grouped, 1):
        print(f"\n{i}. Об'єднаний товар:")
        print(f"   Назва: {product['name']}")
        print(f"   Ціна: {product['price']} грн")
